    public static Bitmap drawTextOnNinePatchDrawable(Context context, String text, String pic) {
        pic="ninepatch/dynamic_route_bubble_left_bottom";
        Bitmap bitmap = null;
        try {
            bitmap = NinePatchUtils.getNinePathchFromAsset(context, pic);
        } catch (Exception e) {

        }
        if (bitmap != null && !TextUtils.isEmpty(text)) {
            NinePatchDrawable ninePatchDrawable = getNinePathDrawableFromBitmap(context, bitmap);
            if (ninePatchDrawable == null) return bitmap;
            android.graphics.Bitmap.Config bitmapConfig = bitmap.getConfig();
            if (bitmapConfig == null) {
                bitmapConfig = Bitmap.Config.RGB_565;
            }
            Paint paint = new Paint(Paint.ANTI_ALIAS_FLAG);
            paint.setColor(Color.WHITE);
            paint.setTextSize(32);
            paint.setDither(true);
            paint.setFilterBitmap(true);
            Rect bounds = new Rect();
            paint.getTextBounds(text, 0, text.length(), bounds);
            int picWidth = (int) paint.measureText(text) + 26;
            int picHeight = bounds.height() + 50;
            bounds = new Rect(0, 0, picWidth, picHeight);
            ninePatchDrawable.setBounds(bounds);
            bitmap = Bitmap.createBitmap(bounds.width(), bounds.height(), bitmapConfig);
            Canvas canvas = new Canvas(bitmap);
            ninePatchDrawable.draw(canvas);
            int height;
            //30 代表 是.9图片不进行拉伸的突出的那个角的高度再加上文字距图片上边缘的间距
            if (pic.contains("bottom")) {
                height = 32 + (picHeight - 32 - 30) / 2;
            } else {
                height = 32 + 30;
            }
            canvas.drawText(text, 13, height, paint);
        }
        return bitmap;
    }

    // 按大小缩放
    public static Bitmap getimage(Context context, Uri uri) {

        BitmapFactory.Options newOpts = new BitmapFactory.Options();
        // 开始读入图片，此时把options.inJustDecodeBounds 设回true了
        newOpts.inJustDecodeBounds = true;
        Bitmap bitmap = null;
        try {
            bitmap = BitmapFactory.decodeStream(context.getContentResolver().openInputStream(uri), null, newOpts);// 此时返回bm为空
            newOpts.inJustDecodeBounds = false;
            int w = newOpts.outWidth;
            int h = newOpts.outHeight;
            float hh = 800f;
            float ww = 480f;
            // 缩放比。由于是固定比例缩放，只用高或者宽其中一个数据进行计算即可
            int be = 1;// be=1表示不缩放
            if (w > h && w > ww) {// 如果宽度大的话根据宽度固定大小缩放
                be = (int) (newOpts.outWidth / ww);
            } else if (w < h && h > hh) {// 如果高度高的话根据宽度固定大小缩放
                be = (int) (newOpts.outHeight / hh);
            }
            if (be < 1) be = 1;
            newOpts.inSampleSize = be;
            // 重新读入图片，注意此时已经把options.inJustDecodeBounds 设回false了
            bitmap = BitmapFactory.decodeStream(context.getContentResolver().openInputStream(uri), null, newOpts);
            String path = getPhotoPathByUri(context, uri);
            int degree = getBitmapDegree(path);
            if (degree != 0) {
                Matrix m = new Matrix();
                int width = bitmap.getWidth();
                int height = bitmap.getHeight();
                m.setRotate(degree); // 旋转angle度
                bitmap = Bitmap.createBitmap(bitmap, 0, 0, width, height, m, true);// 从新生成图片
//                saveBitmap2File(bitmap, path);
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
        return compressImage(bitmap);// 压缩好比例大小后再进行质量压缩
    }

    public static String getPhotoPathByUri(Context context, Uri uri) {
        String path = uri.getPath();
        if (path.startsWith("content")) {
            String[] filePathColumn = {MediaStore.Images.Media.DATA};
            Cursor cursor = context.getContentResolver().query(uri, filePathColumn, null, null, null);
            cursor.moveToFirst();
            int columnIndex = cursor.getColumnIndex(filePathColumn[0]);
            path = cursor.getString(columnIndex);
            cursor.close();
        }
        return path;
    }

    private static int getBitmapDegree(String path) {
        int degree = 0;
        try {
            // 从指定路径下读取图片，并获取其EXIF信息
            ExifInterface exifInterface = new ExifInterface(path);
            // 获取图片的旋转信息
            int orientation = exifInterface.getAttributeInt(ExifInterface.TAG_ORIENTATION, ExifInterface.ORIENTATION_NORMAL);
            switch (orientation) {
                case ExifInterface.ORIENTATION_ROTATE_90:
                    degree = 90;
                    break;
                case ExifInterface.ORIENTATION_ROTATE_180:
                    degree = 180;
                    break;
                case ExifInterface.ORIENTATION_ROTATE_270:
                    degree = 270;
                    break;
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return degree;
    }

    private static int getAngle(Context context, Uri mImageCaptureUri) {
        int angle = 0;
        ContentResolver cr = context.getContentResolver();
        Cursor cursor = cr.query(mImageCaptureUri, null, null, null, null);// 根据Uri从数据库中找
        if (cursor != null) {
            cursor.moveToFirst();// 把游标移动到首位，因为这里的Uri是包含ID的所以是唯一的不需要循环找指向第一个就是了
            String filePath = cursor.getString(cursor.getColumnIndex("_data"));// 获取图片路径
            String orientation = cursor.getString(cursor.getColumnIndex("orientation"));// 获取旋转的角度
            cursor.close();
            if (orientation != null && !"".equals(orientation)) {
                angle = Integer.parseInt(orientation);
            }
        }
        return angle;
    }

    // 图片质量压缩
    private static Bitmap compressImage(Bitmap image) {
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        image.compress(Bitmap.CompressFormat.JPEG, 100, baos);// 质量压缩方法，这里100表示不压缩，把压缩后的数据存放到baos中
        int options = 100;
        while (baos.toByteArray().length / 1024 > 400) { // 循环判断如果压缩后图片是否大于100kb,大于继续压缩
            baos.reset();// 重置baos即清空baos
            image.compress(Bitmap.CompressFormat.JPEG, options, baos);// 这里压缩options%，把压缩后的数据存放到baos中
            options -= 10;// 每次都减少10
        }
        ByteArrayInputStream isBm = new ByteArrayInputStream(baos.toByteArray());// 把压缩后的数据baos存放到ByteArrayInputStream中
        Bitmap bitmap = BitmapFactory.decodeStream(isBm, null, null);// 把ByteArrayInputStream数据生成图片
        return bitmap;
    }

    // 图片按比例大小压缩
    private static Bitmap comp(Bitmap image) {

        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        image.compress(Bitmap.CompressFormat.JPEG, 100, baos);
        if (baos.toByteArray().length / 1024 > 1024) {// 判断如果图片大于1M,进行压缩避免在生成图片（BitmapFactory.decodeStream）时溢出
            baos.reset();// 重置baos即清空baos
            image.compress(Bitmap.CompressFormat.JPEG, 50, baos);// 这里压缩50%，把压缩后的数据存放到baos中
        }
        ByteArrayInputStream isBm = new ByteArrayInputStream(baos.toByteArray());
        BitmapFactory.Options newOpts = new BitmapFactory.Options();
        // 开始读入图片，此时把options.inJustDecodeBounds 设回true了
        newOpts.inJustDecodeBounds = true;
        Bitmap bitmap = BitmapFactory.decodeStream(isBm, null, newOpts);
        newOpts.inJustDecodeBounds = false;
        int w = newOpts.outWidth;
        int h = newOpts.outHeight;
        // 现在主流手机比较多是800*480分辨率，所以高和宽我们设置为
        float hh = 800f;// 这里设置高度为800f
        float ww = 480f;// 这里设置宽度为480f
        // 缩放比。由于是固定比例缩放，只用高或者宽其中一个数据进行计算即可
        int be = 1;// be=1表示不缩放
        if (w > h && w > ww) {// 如果宽度大的话根据宽度固定大小缩放
            be = (int) (newOpts.outWidth / ww);
        } else if (w < h && h > hh) {// 如果高度高的话根据宽度固定大小缩放
            be = (int) (newOpts.outHeight / hh);
        }
        if (be <= 0) be = 1;
        newOpts.inSampleSize = be;// 设置缩放比例
        // 重新读入图片，注意此时已经把options.inJustDecodeBounds 设回false了
        isBm = new ByteArrayInputStream(baos.toByteArray());
        bitmap = BitmapFactory.decodeStream(isBm, null, newOpts);
        return compressImage(bitmap);// 压缩好比例大小后再进行质量压缩
    }

    // 图片转为文件
    public static boolean saveBitmap2File(Bitmap bmp, String dir, String fileName) {
        if (bmp == null || TextUtils.isEmpty(dir) || TextUtils.isEmpty(fileName)) {
            return false;
        }
        File file = new File(dir);
        if (!file.exists()) {
            file.mkdirs();
        }
        OutputStream stream = null;
        try {
//            stream = new FileOutputStream(dir + Constants.SEPARATOR + fileName);
            stream = new FileOutputStream(dir + "//" + fileName);
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
        return bmp.compress(Bitmap.CompressFormat.JPEG, 100, stream);
    }

    public static void saveBitmap2File(Bitmap bitmap, String fileName) {
        if (bitmap == null || TextUtils.isEmpty(fileName)) {
            return;
        }
        File file = new File(fileName);
        File dir = file.getParentFile();
        if (!dir.exists()) {
            dir.mkdirs();
        }
        OutputStream stream = null;
        try {
            stream = new FileOutputStream(fileName);
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
        bitmap.compress(Bitmap.CompressFormat.JPEG, 100, stream);
    }


